//  Comparison Operators

// ==
let x = '5';
let y = 5;
console.log(x == y); 

// ===
let a = '5';
let b = 5;
console.log(a === b);

// !=
let c = '5';
let d = 5;
console.log(c != d);

// !==
let e = '5';
let f = 5;
console.log(e !== f);

// >
let g = 10;
let h = 5;
console.log(g > h);

// <
let i = 3;
let j = 7;
console.log(i < j);

// >=
let k = 10;
let l = 5;
console.log(k >= l);

// <=
let m = 3;
let n = 7;
console.log(m <= n);